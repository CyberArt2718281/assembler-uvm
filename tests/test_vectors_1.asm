# Программа 1: Поэлементное применение >= к двум векторам длины 7
# Вектор A: [5, 10, 15, 20, 25, 30, 35] (адреса 100-106)
# Вектор B: [3, 15, 10, 20, 30, 25, 40] (адреса 200-206)
# Результат записывается в вектор B (адреса 200-206)
# Ожидаемый результат: [1, 0, 1, 1, 0, 1, 0]

# Инициализация вектора A
LOAD_CONST 5 100
LOAD_CONST 10 101
LOAD_CONST 15 102
LOAD_CONST 20 103
LOAD_CONST 25 104
LOAD_CONST 30 105
LOAD_CONST 35 106

# Инициализация вектора B
LOAD_CONST 3 200
LOAD_CONST 15 201
LOAD_CONST 10 202
LOAD_CONST 20 203
LOAD_CONST 30 204
LOAD_CONST 25 205
LOAD_CONST 40 206

# Подготовка базовых адресов
LOAD_CONST 100 500  # Базовый адрес вектора A
LOAD_CONST 200 501  # Базовый адрес для результата

# Выполнение поэлементного сравнения >=
# Элемент 0: A[0] >= B[0]
GTE 0 500 200 501 0

# Элемент 1: A[1] >= B[1]
GTE 1 500 201 501 1

# Элемент 2: A[2] >= B[2]
GTE 2 500 202 501 2

# Элемент 3: A[3] >= B[3]
GTE 3 500 203 501 3

# Элемент 4: A[4] >= B[4]
GTE 4 500 204 501 4

# Элемент 5: A[5] >= B[5]
GTE 5 500 205 501 5

# Элемент 6: A[6] >= B[6]
GTE 6 500 206 501 6
