# Программа 2: Поэлементное применение >= к двум векторам длины 7
# Вектор A: [100, 50, 75, 60, 80, 90, 55] (адреса 100-106)
# Вектор B: [100, 60, 70, 60, 85, 80, 60] (адреса 200-206)
# Результат записывается в вектор B (адреса 200-206)
# Ожидаемый результат: [1, 0, 1, 1, 0, 1, 0]

# Инициализация вектора A
LOAD_CONST 100 100
LOAD_CONST 50 101
LOAD_CONST 75 102
LOAD_CONST 60 103
LOAD_CONST 80 104
LOAD_CONST 90 105
LOAD_CONST 55 106

# Инициализация вектора B
LOAD_CONST 100 200
LOAD_CONST 60 201
LOAD_CONST 70 202
LOAD_CONST 60 203
LOAD_CONST 85 204
LOAD_CONST 80 205
LOAD_CONST 60 206

# Подготовка базовых адресов
LOAD_CONST 100 500  # Базовый адрес вектора A
LOAD_CONST 200 501  # Базовый адрес для результата

# Выполнение поэлементного сравнения >=
GTE 0 500 200 501 0
GTE 1 500 201 501 1
GTE 2 500 202 501 2
GTE 3 500 203 501 3
GTE 4 500 204 501 4
GTE 5 500 205 501 5
GTE 6 500 206 501 6
