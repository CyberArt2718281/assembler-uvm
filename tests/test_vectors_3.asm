# Программа 3: Поэлементное применение >= к двум векторам длины 7
# Вектор A: [7, 7, 7, 7, 7, 7, 7] (адреса 100-106)
# Вектор B: [8, 7, 6, 9, 5, 10, 4] (адреса 200-206)
# Результат записывается в вектор B (адреса 200-206)
# Ожидаемый результат: [0, 1, 1, 0, 1, 0, 1]

# Инициализация вектора A (все элементы = 7)
LOAD_CONST 7 100
LOAD_CONST 7 101
LOAD_CONST 7 102
LOAD_CONST 7 103
LOAD_CONST 7 104
LOAD_CONST 7 105
LOAD_CONST 7 106

# Инициализация вектора B
LOAD_CONST 8 200
LOAD_CONST 7 201
LOAD_CONST 6 202
LOAD_CONST 9 203
LOAD_CONST 5 204
LOAD_CONST 10 205
LOAD_CONST 4 206

# Подготовка базовых адресов
LOAD_CONST 100 500  # Базовый адрес вектора A
LOAD_CONST 200 501  # Базовый адрес для результата

# Выполнение поэлементного сравнения >=
GTE 0 500 200 501 0
GTE 1 500 201 501 1
GTE 2 500 202 501 2
GTE 3 500 203 501 3
GTE 4 500 204 501 4
GTE 5 500 205 501 5
GTE 6 500 206 501 6
