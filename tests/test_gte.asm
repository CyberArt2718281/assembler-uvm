# Тест операции GTE (>=)
# Проверяем различные случаи сравнения

# Инициализируем тестовые значения
LOAD_CONST 10 100   # value1 = 10
LOAD_CONST 5 101    # value2 = 5  (10 >= 5 = true)
LOAD_CONST 15 102   # value3 = 15 (10 >= 15 = false)
LOAD_CONST 10 103   # value4 = 10 (10 >= 10 = true)

# Подготовка базовых адресов для GTE
LOAD_CONST 100 500  # base_addr для первого операнда
LOAD_CONST 300 501  # base_addr для результата

# Тест 1: 10 >= 5 (должно быть 1)
GTE 0 500 101 501 0

# Тест 2: 10 >= 15 (должно быть 0)
GTE 0 500 102 501 1

# Тест 3: 10 >= 10 (должно быть 1)
GTE 0 500 103 501 2
