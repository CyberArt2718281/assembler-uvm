# Тестовая программа со всеми примерами из спецификации УВМ

# Тест 1: LOAD_CONST (A=111, B=37, C=80)
# Ожидается: 0xEF, 0x12, 0x40, 0x01, 0x00, 0x00
LOAD_CONST 37 80

# Тест 2: READ_MEM (A=40, B=82, C=877, D=682)
# Ожидается: 0x28, 0xA9, 0xB6, 0x01, 0x00, 0x54, 0x05, 0x00, 0x00
READ_MEM 82 877 682

# Тест 3: WRITE_MEM (A=101, B=870, C=912)
# Ожидается: 0x65, 0xB3, 0x01, 0x00, 0x20, 0x07, 0x00, 0x00
WRITE_MEM 870 912

# Тест 4: GTE (A=68, B=42, C=671, D=242, E=575, F=150)
# Ожидается: 0x44, 0x95, 0x4F, 0x81, 0x80, 0xE4, 0x81, 0x80, 0xF8, 0x11, 0x80, 0xC0, 0x12
GTE 42 671 242 575 150
