# Программа копирования массива
# Копируем массив из 5 элементов с адреса 100 на адрес 200

# Инициализируем исходный массив
LOAD_CONST 10 100   # array[0] = 10
LOAD_CONST 20 101   # array[1] = 20
LOAD_CONST 30 102   # array[2] = 30
LOAD_CONST 40 103   # array[3] = 40
LOAD_CONST 50 104   # array[4] = 50

# Копируем элементы
# Используем адрес 500 как временную ячейку для хранения базового адреса

# Копируем первый элемент
LOAD_CONST 100 500  # temp = адрес источника (100)
READ_MEM 0 500 600  # читаем array[100+0] в 600
WRITE_MEM 600 200   # пишем в новый массив[0]

# Копируем второй элемент
READ_MEM 1 500 600  # читаем array[100+1] в 600
WRITE_MEM 600 201   # пишем в новый массив[1]

# Копируем третий элемент
READ_MEM 2 500 600  # читаем array[100+2] в 600
WRITE_MEM 600 202   # пишем в новый массив[2]

# Копируем четвёртый элемент
READ_MEM 3 500 600  # читаем array[100+3] в 600
WRITE_MEM 600 203   # пишем в новый массив[3]

# Копируем пятый элемент
READ_MEM 4 500 600  # читаем array[100+4] в 600
WRITE_MEM 600 204   # пишем в новый массив[4]
